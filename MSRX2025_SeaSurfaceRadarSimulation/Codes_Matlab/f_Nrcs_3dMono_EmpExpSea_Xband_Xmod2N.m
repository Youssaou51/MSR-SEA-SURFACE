function [Nrcs_Mdl_VV, Nrcs_Mdl_HH] = f_Nrcs_3dMono_EmpExpSea_Xband_Xmod2N (Th_i_d, u10_ms, windDir_d) 

%--------------------------------------------------------------------------
% FUNCTION : f_Nrcs_3dMono_EmpExpSea_Xband_Xmod2N
%--------------------------------------------------------------------------
% Calculates the backscattering normalized radar cross section (NRCS, also 
% called scattering coefficient)of a (2D) sea surface for 3D problems, 
% in VV polarization and in X band (10.00 GHz), with respect to the empirical 
% model of Nirchio and Venafra (EuJRS 2013) "XMOD2", for incidence angles 18°-46°:
% - Th_i_d must be between 18°-46°
% - u10_ms must be between 2-25 m/s
% - (winddir_d must be between 0°-360°
%--------------------------------------------------------------------------
% Input variables:
% Th_i_d        : incidence angle with respect to the vertical z (deg.) [Vector]
% u10_ms        : wind speed at 10 m above the sea surface (m/s) [scalar]
% windDir_d     : wind direction with respect to the incidence azimuth 
%                 direction, \phi-\phi0 (deg.) [scalar]
%--------------------------------------------------------------------------
% Output variables:
% Nrcs_Mdl_VV  : NRCS with respect to considered model in VV polarization [Vector - lin. scale]
% Nrcs_Mdl_HH  : NRCS with respect to considered model in HH polarization [Vector - lin. scale]
%--------------------------------------------------------------------------
% COMMENTS:
% V = TM = p = //
% H = TE = s = \perp
% F. Nirchio and S. Venafra, "XMOD2 - An improved geophysical model function 
% to retrieve sea surface wind fields from Cosmo-SkyMed X-band data", 
% European Journal of Remote Sensing, vol. 46, pp. 583-595, 2013
%--------------------------------------------------------------------------
% Called functions:
% (none)
%--------------------------------------------------------------------------
% Nicolas PINEL, 07/2013
%--------------------------------------------------------------------------

% Applicability domain of the empirical/experimental model (XMOD2-Nirchio here):
It = find( Th_i_d < 18 | Th_i_d > 46 ) ;
if ~isempty(It)
    disp(Th_i_d(It)) ;
    disp('/!\ 18° <= Th_i_d <= 46° /!\') ;
    pause(1) ;
end
if (u10_ms < 02) || (u10_ms > 25)
    disp(u10_ms) ;
    disp('/!\ 02 m/s <= u10_ms <= 25 m/s /!\') ;
    pause(1) ;
end

% ci [2013_Nirchio_EuJRS,Tab.1-2]:
if u10_ms <= 7
    c01 =  6.657480 ; %±...
    c02 = -0.527524 ; %±...
    c03 =  0.007124 ; %±...
    c04 = -4.650782 ; %±...
    c05 =  0.402273 ; %±...
    c06 = -0.006065 ; %±...
    c07 = -0.258321 ; %±...
    c08 =  0.013675 ; %±...
    c09 = -0.000186 ; %±...
    c10 =  0.051664 ; %±...
    c11 = -0.002735 ; %±...
    c12 =  0.000037 ; %±...
    c13 = -1.334011 ; %±...
    c14 =  0.098156 ; %±...
    c15 = -0.001013 ; %±...
    c16 =  0.316948 ; %±...
    c17 = -0.020622 ; %±...
    c18 =  0.000283 ; %±...
else

    c01 =  3.152255 ; %±...
    c02 = -0.2694191 ; %±...
    c03 =  0.0029979 ; %±...
    c04 = -0.450287 ; %±...
    c05 =  0.0928452 ; %±...
    c06 = -0.001101 ; %±...
    c07 = -0.0228304 ; %±...
    c08 =  0.0016691 ; %±...
    c09 = -0.000023 ; %±...
    c10 =  0.0019511 ; %±...
    c11 = -0.0001425 ; %±...
    c12 =  0.000002 ; %±...
    c13 =  2.0670443 ; %±...
    c14 = -0.1309205 ; %±...
    c15 =  0.0023609 ; %±...
    c16 = -0.1698661 ; %±...
    c17 =  0.0124482 ; %±...
    c18 = -0.000211 ; %±...
end

Al = Th_i_d ;
p = 1 ;

% Functions [2010_Nirchio_IGARSS,eq.6-7]:
Bt = c01 + c02 * Al + c03 * Al.^2 ;
Gm = c04 + c05 * Al + c06 * Al.^2 ;

% Calculation of B_0, B_1, B_2 terms [2010_Nirchio_IGARSS,eq.3-5]:
B_0 = 10.^Bt .* u10_ms.^Gm ;
B_1 = c07 + c08 * Al + c09 * Al.^2 + (c10 + c11*Al + c12*Al.^2) * u10_ms ;
B_2 = c13 + c14 * Al + c15 * Al.^2 + (c16 + c17*Al + c18*Al.^2) * u10_ms ;

% NRCS in linear scale :
Nrcs_Mdl_VV = B_0 .* ( 1 + B_1 * cosd(1*windDir_d) + B_2 * cosd(2*windDir_d) ).^p ;
Nrcs_Mdl_HH = zeros(size(Nrcs_Mdl_VV)) ;
%
Nrcs_Mdl_VV(It) = 0 ; % ou plutôt NaN
Nrcs_Mdl_HH(It) = 0 ; % ou plutôt NaN
